--
-- Tablas
--

CREATE TABLE public.accounts (
    id bigint NOT NULL,
    current_balance numeric(19,2) NOT NULL,
    customer_id bigint NOT NULL,
    initial_balance numeric(19,2) NOT NULL,
    number character varying(255) NOT NULL,
    status boolean NOT NULL,
    type character varying(255) NOT NULL,
    CONSTRAINT accounts_type_check CHECK (((type)::text = ANY ((ARRAY['SAVINGS'::character varying, 'CURRENT'::character varying])::text[])))
);

ALTER TABLE public.accounts ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.accounts_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

CREATE TABLE public.customer_snapshot (
    customer_id bigint NOT NULL,
    identification character varying(255) NOT NULL,
    name character varying(255) NOT NULL,
    status boolean NOT NULL
);

CREATE TABLE public.customers (
    password character varying(255) NOT NULL,
    status boolean NOT NULL,
    customer_id bigint NOT NULL
);

CREATE TABLE public.movements (
    id bigint NOT NULL,
    account_id bigint NOT NULL,
    balance numeric(19,2) NOT NULL,
    date timestamp(6) without time zone NOT NULL,
    type character varying(255) NOT NULL,
    value numeric(19,2) NOT NULL,
    CONSTRAINT movements_type_check CHECK (((type)::text = ANY ((ARRAY['CREDIT'::character varying, 'DEBIT'::character varying])::text[])))
);

ALTER TABLE public.movements ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.movements_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

CREATE TABLE public.persons (
    id bigint NOT NULL,
    address character varying(255) NOT NULL,
    gender character varying(255) NOT NULL,
    identification character varying(255) NOT NULL,
    name character varying(255) NOT NULL,
    phone character varying(255) NOT NULL,
    CONSTRAINT persons_gender_check CHECK (((gender)::text = ANY ((ARRAY['MALE'::character varying, 'FEMALE'::character varying, 'OTHER'::character varying])::text[])))
);

ALTER TABLE public.persons ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.persons_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

